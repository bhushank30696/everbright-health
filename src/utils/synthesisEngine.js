export function getQuarterlyModifier(q, providerId) {
  let hash = 0;
  for (let i = 0; i < providerId.length; i++) {
    hash = providerId.charCodeAt(i) + ((hash << 5) - hash);
  }
  let rand = (Math.abs(hash) % 100) / 100;

  if (q === 'Q1') {
    return { itOffset: 0, ticketOffset: 0, durationOffset: 0, satOffset: 0 };
  } else if (q === 'Q2') {
    return {
      itOffset: -1.2 + rand * 0.4,
      ticketOffset: -2.5 + rand * 1.0,
      durationOffset: -2.0 + rand * 0.8,
      satOffset: 0.15 + rand * 0.1
    };
  } else if (q === 'Q3') {
    return {
      itOffset: 1.5 + rand * 0.6,
      ticketOffset: 3.5 + rand * 1.5,
      durationOffset: 2.5 + rand * 1.0,
      satOffset: -0.25 - rand * 0.1
    };
  } else if (q === 'Q4') {
    return {
      itOffset: -2.8 + rand * 0.4,
      ticketOffset: -6.0 + rand * 2.0,
      durationOffset: -4.0 + rand * 1.5,
      satOffset: 0.45 + rand * 0.1
    };
  }
}

