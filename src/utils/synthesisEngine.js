export function generateProvidersForQuarter(q, customBaseProviders = null) {
  let list = [];
  const base = customBaseProviders || BASE_PROVIDERS;
  let baseCount = base.length;
  let targetCount = baseCount;
  if (q === 'Q3') targetCount = Math.round(baseCount * 1.25); // Simulated surge based on dataset size

  for (let i = 0; i < targetCount; i++) {
    let baseItem = base[i % baseCount];
    let p = JSON.parse(JSON.stringify(baseItem));
    
    if (i >= baseCount) {
      p['Provider ID'] = 'P' + String(i + 1).padStart(3, '0');
    }

    let mods = getQuarterlyModifier(q, p['Provider ID']);
    
    p['IT Provisioning Days'] = Math.max(2.0, Math.round((p['IT Provisioning Days'] + mods.itOffset) * 10) / 10);
    p['Avg Ticket Resolution Time (hrs)'] = Math.max(8.0, Math.round((p['Avg Ticket Resolution Time (hrs)'] + mods.ticketOffset) * 10) / 10);
    
    if (q === 'Q4') {
      p['Contracting Days'] = Math.max(1.5, Math.round((p['Contracting Days'] - 0.8) * 10) / 10);
      p['Compliance Days'] = Math.max(1.5, Math.round((p['Compliance Days'] - 0.5) * 10) / 10);
      p['Training Days'] = Math.max(1.5, Math.round((p['Training Days'] - 0.5) * 10) / 10);
    } else if (q === 'Q3') {
      p['Compliance Days'] = Math.max(2.0, Math.round((p['Compliance Days'] + 0.8) * 10) / 10);
    }

    let totalDuration = Math.round((p['Contracting Days'] + p['IT Provisioning Days'] + p['Compliance Days'] + p['Training Days']) * 10) / 10;
    p['Onboarding Duration (days)'] = Math.round(totalDuration);

    let isBreached = p['SLA Breach (Y/N)'] === 'Y';
    
    let tasksPct = p['% Tasks Completed On Time'];
    if (tasksPct === '' || !tasksPct) {
      tasksPct = isBreached ? 75 : 88;
    }
    p['% Tasks Completed On Time'] = tasksPct;
    p.support_efficiency = Math.round((tasksPct / p['Avg Ticket Resolution Time (hrs)']) * 100) / 100;

    let escalations = p['# Escalations'];
    if (escalations === '' || escalations === undefined || escalations === null) {
      escalations = isBreached ? 4 : 1;
    }
    p['# Escalations'] = escalations;
    p.escalation_burden = Math.round(escalations * p['Avg Ticket Resolution Time (hrs)'] * 10) / 10;

    let riskScore = 0;
    if (p['Avg Ticket Resolution Time (hrs)'] > 22) riskScore++;
    if (p['# Escalations'] >= 3) riskScore++;
    if (p['IT Provisioning Days'] > 7) riskScore++;
    p.risk_score = riskScore;

    if (isBreached) {
      p.sla_risk = 'Breached';
    } else {
      if (riskScore >= 2) {
        p.sla_risk = 'High Risk';
      } else if (riskScore === 1) {
        p.sla_risk = 'Medium Risk';
      } else {
        p.sla_risk = 'On Track';
      }
    }

    p.it_share = Math.round((p['IT Provisioning Days'] / totalDuration) * 100);

    let steps = [
      { label: 'Contracting', days: p['Contracting Days'] },
      { label: 'IT Provisioning', days: p['IT Provisioning Days'] },
      { label: 'Compliance', days: p['Compliance Days'] },
      { label: 'Training', days: p['Training Days'] }
    ];
    steps.sort((a, b) => b.days - a.days);
    p.dominant_step = steps[0].label;

    let months = {
      'Q1': ['Jan', 'Feb', 'Mar'],
      'Q2': ['Apr', 'May', 'Jun'],
      'Q3': ['Jul', 'Aug', 'Sep'],
      'Q4': ['Oct', 'Nov', 'Dec']
    };
    p['Start Date'] = months[q][i % 3] + ' ' + (10 + (i % 18)) + ', 2025';
    p['Satisfaction Score (1-5)'] = Math.min(5.0, Math.max(1.0, Math.round((p['Satisfaction Score (1-5)'] + mods.satOffset) * 10) / 10));

    // Dynamic numeric risk index (0 to 100) based on SLA status and risk items
    let scoreBase = isBreached ? 75 : 10;
    scoreBase += riskScore * 10;
    scoreBase += Math.min(10, Math.round((p['IT Provisioning Days'] / 10) * 10));
    p.calculated_risk_index = Math.min(99, Math.max(0, scoreBase));

    p.ProviderName = getProviderName(p['Provider ID']);

    list.push(p);
  }

  let mktAverages = {};
  ['TX', 'CA', 'FL'].forEach(mkt => {
    let mktProvs = list.filter(p => p.Market === mkt);
    let avgIt = mktProvs.length ? (mktProvs.reduce((sum, p) => sum + p['IT Provisioning Days'], 0) / mktProvs.length) : 6.5;
    mktAverages[mkt] = avgIt;
  });

  list.forEach(p => {
    p.it_vs_mkt = Math.round((p['IT Provisioning Days'] - mktAverages[p.Market]) * 10) / 10;
  });

  return list;
}

export function recomputeDataAggregates(allProvidersInQuarter, activeState, activeQuarter) {
  let filtered = allProvidersInQuarter;
  if (activeState !== 'all') {
    filtered = allProvidersInQuarter.filter(p => p.Market === activeState);
  }

  let total = filtered.length;
  let breachedCount = filtered.filter(p => p['SLA Breach (Y/N)'] === 'Y').length;
  let atRiskCount = filtered.filter(p => p.risk_score >= 2 && p['SLA Breach (Y/N)'] === 'N').length;
  
  let avgOnboarding = total ? (filtered.reduce((sum, p) => sum + p['Onboarding Duration (days)'], 0) / total) : 0;
  let avgSat = total ? (filtered.reduce((sum, p) => sum + p['Satisfaction Score (1-5)'], 0) / total) : 0;
  let avgEsc = total ? (filtered.reduce((sum, p) => sum + p['# Escalations'], 0) / total) : 0;

  let kpis = {
    avg_onboarding: Math.round(avgOnboarding * 10) / 10,
    sla_breach_pct: total ? Math.round((breachedCount / total) * 1000) / 10 : 0,
    avg_satisfaction: Math.round(avgSat * 100) / 100,
    avg_escalations: Math.round(avgEsc * 10) / 10,
    at_risk_count: atRiskCount,
    total_providers: total
  };

  let market_summary = ['CA', 'FL', 'TX'].map(mkt => {
    let mktProvs = allProvidersInQuarter.filter(p => p.Market === mkt);
    let mktTotal = mktProvs.length;
    if (mktTotal === 0) return { Market: mkt, providers: 0, breach_rate: 0, avg_onboarding: 0, avg_it_days: 0, avg_ticket: 0, avg_sat: 0, avg_esc: 0, avg_burden: 0, avg_efficiency: 0 };
    
    let mktBreached = mktProvs.filter(p => p['SLA Breach (Y/N)'] === 'Y').length;
    let mktOnboarding = mktProvs.reduce((sum, p) => sum + p['Onboarding Duration (days)'], 0) / mktTotal;
    let mktIt = mktProvs.reduce((sum, p) => sum + p['IT Provisioning Days'], 0) / mktTotal;
    let mktTicket = mktProvs.reduce((sum, p) => sum + p['Avg Ticket Resolution Time (hrs)'], 0) / mktTotal;
    let mktSat = mktProvs.reduce((sum, p) => sum + p['Satisfaction Score (1-5)'], 0) / mktTotal;
    let mktEsc = mktProvs.reduce((sum, p) => sum + p['# Escalations'], 0) / mktTotal;
    let mktBurden = mktProvs.reduce((sum, p) => sum + p.escalation_burden, 0) / mktTotal;
    let mktEff = mktProvs.reduce((sum, p) => sum + p.support_efficiency, 0) / mktTotal;

    return {
      Market: mkt,
      providers: mktTotal,
      breach_rate: Math.round((mktBreached / mktTotal) * 100) / 100,
      avg_onboarding: Math.round(mktOnboarding * 10) / 10,
      avg_it_days: Math.round(mktIt * 10) / 10,
      avg_ticket: Math.round(mktTicket * 10) / 10,
      avg_sat: Math.round(mktSat * 100) / 100,
      avg_esc: Math.round(mktEsc * 10) / 10,
      avg_burden: Math.round(mktBurden * 10) / 10,
      avg_efficiency: Math.round(mktEff * 100) / 100
    };
  });

  let delayReasons = ['IT Access', 'Internal Handoff', 'Missing Docs', 'Scheduling', 'Vendor SLA'];
  let delay_reasons = delayReasons.map(dr => {
    let drProvs = filtered.filter(p => p['Primary Delay Reason'] === dr);
    let drCount = drProvs.length;
    if (drCount === 0) return { 'Primary Delay Reason': dr, count: 0, breach_rate: 0, avg_sat: 0, avg_ticket: 0, avg_onboarding: 0 };
    
    let drBreached = drProvs.filter(p => p['SLA Breach (Y/N)'] === 'Y').length;
    let drSat = drProvs.reduce((sum, p) => sum + p['Satisfaction Score (1-5)'], 0) / drCount;
    let drTicket = drProvs.reduce((sum, p) => sum + p['Avg Ticket Resolution Time (hrs)'], 0) / drCount;
    let drOnboarding = drProvs.reduce((sum, p) => sum + p['Onboarding Duration (days)'], 0) / drCount;

    return {
      'Primary Delay Reason': dr,
      count: drCount,
      breach_rate: Math.round((drBreached / drCount) * 100) / 100,
      avg_sat: Math.round(drSat * 100) / 100,
      avg_ticket: Math.round(drTicket * 10) / 10,
      avg_onboarding: Math.round(drOnboarding * 10) / 10
    };
  });

  let escTypes = ['Compliance', 'IT', 'Operations', 'Other'];
  let escalation_types = escTypes.map(et => {
    let etProvs = filtered.filter(p => p['Escalation Type'] === et);
    let etCount = etProvs.length;
    if (etCount === 0) return { 'Escalation Type': et, count: 0, breach_rate: 0, avg_sat: 0, avg_ticket: 0 };

    let etBreached = etProvs.filter(p => p['SLA Breach (Y/N)'] === 'Y').length;
    let etSat = etProvs.reduce((sum, p) => sum + p['Satisfaction Score (1-5)'], 0) / etCount;
    let etTicket = etProvs.reduce((sum, p) => sum + p['Avg Ticket Resolution Time (hrs)'], 0) / etCount;

    return {
      'Escalation Type': et,
      count: etCount,
      breach_rate: Math.round((etBreached / etCount) * 100) / 100,
      avg_sat: Math.round(etSat * 100) / 100,
      avg_ticket: Math.round(etTicket * 10) / 10
    };
  });

  let stepLabels = ['Contracting', 'IT Provisioning', 'Compliance', 'Training'];
  let step_avgs = {};
  stepLabels.forEach(s => {
    let key = s + ' Days';
    let avg = total ? (filtered.reduce((sum, p) => sum + p[key], 0) / total) : 0;
    step_avgs[s] = Math.round(avg * 10) / 10;
  });

  let months = {
    'Q1': ['Jan', 'Feb', 'Mar'],
    'Q2': ['Apr', 'May', 'Jun'],
    'Q3': ['Jul', 'Aug', 'Sep'],
    'Q4': ['Oct', 'Nov', 'Dec']
  };
  let monthly_trend = months[activeQuarter].map(m => {
    let mProvs = filtered.filter(p => p['Start Date'].startsWith(m));
    let mCount = mProvs.length;
    if (mCount === 0) return { start_month: m, count: 0, breach_rate: 0, avg_onboarding: 0 };
    
    let mBreached = mProvs.filter(p => p['SLA Breach (Y/N)'] === 'Y').length;
    let mOnboarding = mProvs.reduce((sum, p) => sum + p['Onboarding Duration (days)'], 0) / mCount;

    return {
      start_month: m,
      count: mCount,
      breach_rate: Math.round((mBreached / mCount) * 100) / 100,
      avg_onboarding: Math.round(mOnboarding * 10) / 10
    };
  });

  let risk_distribution = [0, 1, 2, 3].map(score => {
    let scoreProvs = filtered.filter(p => p.risk_score === score);
    let scoreCount = scoreProvs.length;
    if (scoreCount === 0) return { risk_score: score, count: 0, breach_rate: 0 };

    let scoreBreached = scoreProvs.filter(p => p['SLA Breach (Y/N)'] === 'Y').length;
    return {
      risk_score: score,
      count: scoreCount,
      breach_rate: Math.round((scoreBreached / scoreCount) * 100) / 100
    };
  });

  let delay_by_market = [];
  let esc_by_market = [];
  ['TX', 'CA', 'FL'].forEach(mkt => {
    delayReasons.forEach(dr => {
      let segment = allProvidersInQuarter.filter(p => p.Market === mkt && p['Primary Delay Reason'] === dr);
      let segCount = segment.length;
      if (segCount > 0) {
        let segBreached = segment.filter(p => p['SLA Breach (Y/N)'] === 'Y').length;
        delay_by_market.push({
          Market: mkt,
          'Primary Delay Reason': dr,
          count: segCount,
          breach_rate: segBreached / segCount
        });
      }
    });

    escTypes.forEach(et => {
      let segment = allProvidersInQuarter.filter(p => p.Market === mkt && p['Escalation Type'] === et);
      let segCount = segment.length;
      if (segCount > 0) {
        let segBreached = segment.filter(p => p['SLA Breach (Y/N)'] === 'Y').length;
        esc_by_market.push({
          Market: mkt,
          'Escalation Type': et,
          count: segCount,
          breach_rate: segBreached / segCount
        });
      }
    });
  });

  return {
    kpis,
    market_summary,
    delay_reasons,
    escalation_types,
    step_avgs,
    monthly_trend,
    risk_distribution,
    delay_by_market,
    esc_by_market
  };
}
